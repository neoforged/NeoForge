package net.minecraftforge.common.data;

import net.minecraft.core.HolderLookup;
import net.minecraft.core.registries.BuiltInRegistries;
import net.minecraft.core.registries.Registries;
import net.minecraft.data.PackOutput;
import net.minecraft.data.tags.IntrinsicHolderTagsProvider;
import net.minecraft.world.level.block.Block;
import net.minecraft.world.level.block.entity.BlockEntity;
import net.minecraft.world.level.block.entity.BlockEntityType;
import org.jetbrains.annotations.Nullable;

import java.util.concurrent.CompletableFuture;

public abstract class BlockEntityTagsProvider extends IntrinsicHolderTagsProvider<BlockEntityType<?>>
{
    public BlockEntityTagsProvider(PackOutput output, CompletableFuture<HolderLookup.Provider> lookupProvider, String modId, @Nullable ExistingFileHelper existingFileHelper)
    {
        super(output, Registries.BLOCK_ENTITY_TYPE, lookupProvider, blockEntity -> BuiltInRegistries.BLOCK_ENTITY_TYPE.getResourceKey(blockEntity).orElseThrow(), modId, existingFileHelper);
    }
}
